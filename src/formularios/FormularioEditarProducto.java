    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        abm.setFormularioEditarProducto(this);
        abm.setPrincipalProducto(principalProducto);
        if (abm.ejecutarEditar()) {
            principalProducto.getTablaProducto().setEstadoConsulta(0);
            principalProducto.getTablaProducto().ejecutarRellenarTabla();
        }
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

