    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        abm.setFormularioRegistrarPrecioProducto(this);
        abm.setPrincipalProducto(principalProducto);
        if (abm.ejecutarRegistrarPrecio()) {
            principalProducto.getTablaProducto().setEstadoConsulta(0);
            principalProducto.getTablaProducto().ejecutarRellenarTabla();
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

